
CREATE TABLE CARS
(
  CARS_ID       VARCHAR(50) NOT NULL COMMENT '유니크_ID',
  YEAR          VARCHAR(50) NOT NULL COMMENT '연식',
  NAME          VARCHAR(50) NOT NULL COMMENT '자동차이름',
  CARS_BRAND_ID VARCHAR(50) NOT NULL COMMENT '유니크_ID',
  CARS_NAME_ID  VARCHAR(50) NOT NULL COMMENT '자동차이름',
  PRIMARY KEY (CARS_ID)
) COMMENT '자동차';

CREATE TABLE CARS_BRAND
(
  CARS_BRAND_ID VARCHAR(50) NOT NULL COMMENT '유니크_ID',
  CARS_BRAND    VARCHAR(50) NOT NULL COMMENT '회사이름',
  PRIMARY KEY (CARS_BRAND_ID)
) COMMENT '회사이름';

CREATE TABLE CARS_NAME
(
  CARS_BRAND_ID VARCHAR(50) NOT NULL COMMENT '유니크_ID',
  CARS_NAME_ID  VARCHAR(50) NOT NULL COMMENT '유니크_ID',
  PRIMARY KEY (CARS_NAME_ID)
) COMMENT '자동차이름';

CREATE TABLE CARS_OPTION
(
  CARS_ID        VARCHAR(50) NOT NULL COMMENT '유니크_ID',
  OPTION_NAME_ID VARCHAR(50) NOT NULL COMMENT '유니크_ID'
) COMMENT '자동차_옵션';

CREATE TABLE CARS_YEAR
(
  CARS_ID   VARCHAR(50) NOT NULL COMMENT '유니크_ID',
  CARS_YEAR INTGER      NOT NULL COMMENT '연식'
) COMMENT '연식';

CREATE TABLE OPTION_CODE
(
  OPTION_NAME    VARCHAR(500) NOT NULL COMMENT '옵션_이름',
  OPTION_NAME_ID VARCHAR(50)  NOT NULL COMMENT '유니크_ID',
  PRIMARY KEY (OPTION_NAME_ID)
) COMMENT '옵션_코드';

ALTER TABLE CARS_OPTION
  ADD CONSTRAINT FK_CARS_TO_CARS_OPTION
    FOREIGN KEY (CARS_ID)
    REFERENCES CARS (CARS_ID);

ALTER TABLE CARS_OPTION
  ADD CONSTRAINT FK_OPTION_CODE_TO_CARS_OPTION
    FOREIGN KEY (OPTION_NAME_ID)
    REFERENCES OPTION_CODE (OPTION_NAME_ID);

ALTER TABLE CARS_NAME
  ADD CONSTRAINT FK_CARS_BRAND_TO_CARS_NAME
    FOREIGN KEY (CARS_BRAND_ID)
    REFERENCES CARS_BRAND (CARS_BRAND_ID);

ALTER TABLE CARS_YEAR
  ADD CONSTRAINT FK_CARS_TO_CARS_YEAR
    FOREIGN KEY (CARS_ID)
    REFERENCES CARS (CARS_ID);

ALTER TABLE CARS
  ADD CONSTRAINT FK_CARS_BRAND_TO_CARS
    FOREIGN KEY (CARS_BRAND_ID)
    REFERENCES CARS_BRAND (CARS_BRAND_ID);

ALTER TABLE CARS
  ADD CONSTRAINT FK_CARS_NAME_TO_CARS
    FOREIGN KEY (CARS_NAME_ID)
    REFERENCES CARS_NAME (CARS_NAME_ID);
