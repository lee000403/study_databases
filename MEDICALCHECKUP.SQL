
CREATE TABLE GENDER
(
  GENDER    VARCHAR(50) NOT NULL COMMENT '성별',
  GENDER_ID VARCHAR(50) NOT NULL COMMENT '성별_ID',
  PRIMARY KEY (GENDER_ID)
) COMMENT '성별';

CREATE TABLE MAINTAIN
(
  MAINTAIN            VARCHAR(50) NOT NULL COMMENT '유지방안',
  MIANTAIN_ID         VARCHAR(50) NOT NULL COMMENT '유지방안_ID',
  MAINTAIN_COMMENT_ID VARCHAR(50) NOT NULL COMMENT '유지방안_메모_ID',
  MAINTAIN_BOOL_ID    VARCHAR(50) NOT NULL COMMENT '유지방안_유무_ID',
  PRIMARY KEY (MIANTAIN_ID)
) COMMENT '유지방안';

CREATE TABLE MAINTAIN_BOOL
(
  MAINTAIN_BOOL    BOOLEAN     NOT NULL COMMENT '유지방안_유무',
  MAINTAIN_BOOL_ID VARCHAR(50) NOT NULL COMMENT '유지방안_유무_ID',
  PRIMARY KEY (MAINTAIN_BOOL_ID)
) COMMENT '유지방안_유무';

CREATE TABLE MAINTAIN_CODE
(
  MAINTAIN_COMMENT    VARCHAR(50) NOT NULL COMMENT '유지방안_메모',
  MAINTAIN_COMMENT_ID VARCHAR(50) NOT NULL COMMENT '유지방안_메모_ID',
  PRIMARY KEY (MAINTAIN_COMMENT_ID)
) COMMENT '유지방안_코드';

CREATE TABLE MEMBERS
(
  AGE_WEIGHT       INTEGER      NOT NULL COMMENT '나이',
  BLOOD            VARCHAR(50) NOT NULL COMMENT '혈압',
  RE_RESULT        VARCHAR(50) NOT NULL COMMENT '재검진항목',
  MAINTAIN         VARCHAR(50) NOT NULL COMMENT '유지방안',
  NAME             VARCHAR(50) NOT NULL COMMENT '이름',
  MEMBERS_ID       VARCHAR(50) NOT NULL COMMENT '회원_ID',
  GENDER_ID        VARCHAR(50) NOT NULL COMMENT '성별_ID',
  HEIGHT_WEIGHT    VARCHAR(50) NOT NULL COMMENT '키_체중',
  RESULT_ID        VARCHAR(50) NOT NULL COMMENT '검진결과_ID',
  MIANTAIN_BOOL_ID VARCHAR(50) NOT NULL COMMENT '유지방안_유무_ID',
  PRIMARY KEY (MEMBERS_ID)
) COMMENT '회원 정보';

CREATE TABLE RE_RESULT
(
  MEMBERS_ID          VARCHAR(50) NOT NULL COMMENT '회원_ID',
  RE_RESULT_SELECT_ID VARCHAR(50) NOT NULL COMMENT '재검진항목_ID'
) COMMENT '재검진_항목';

CREATE TABLE RE_RESULT_CODE
(
  RE_RESULT_SELECT    VARCHAR(50) NOT NULL COMMENT '재검진항목',
  RE_RESULT_SELECT_ID VARCHAR(50) NOT NULL COMMENT '재검진항목_ID',
  PRIMARY KEY (RE_RESULT_SELECT_ID)
) COMMENT '재검진_항목_코드';

CREATE TABLE RESULT
(
  RESULT    BOOLEAN     NOT NULL COMMENT '검진결과',
  RESULT_ID VARCHAR(50) NOT NULL COMMENT '검진결과_ID',
  PRIMARY KEY (RESULT_ID)
) COMMENT '검진결과';

