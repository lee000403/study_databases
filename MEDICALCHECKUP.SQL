    FOREIGN KEY (RESULT_ID)
    REFERENCES RESULT (RESULT_ID);

ALTER TABLE RE_RESULT
  ADD CONSTRAINT FK_MEMBERS_TO_RE_RESULT
    FOREIGN KEY (MEMBERS_ID)
    REFERENCES MEMBERS (MEMBERS_ID);

ALTER TABLE RE_RESULT
  ADD CONSTRAINT FK_RE_RESULT_CODE_TO_RE_RESULT
    FOREIGN KEY (RE_RESULT_SELECT_ID)
    REFERENCES RE_RESULT_CODE (RE_RESULT_SELECT_ID);

ALTER TABLE MEMBERS
  ADD CONSTRAINT FK_MAINTAIN_TO_MEMBERS
    FOREIGN KEY (MIANTAIN_BOOL_ID)
    REFERENCES MAINTAIN (MIANTAIN_ID);

ALTER TABLE MAINTAIN
  ADD CONSTRAINT FK_MAINTAIN_CODE_TO_MAINTAIN
    FOREIGN KEY (MAINTAIN_COMMENT_ID)
    REFERENCES MAINTAIN_CODE (MAINTAIN_COMMENT_ID);

ALTER TABLE MAINTAIN
  ADD CONSTRAINT FK_MAINTAIN_BOOL_TO_MAINTAIN
    FOREIGN KEY (MAINTAIN_BOOL_ID)
    REFERENCES MAINTAIN_BOOL (MAINTAIN_BOOL_ID);
